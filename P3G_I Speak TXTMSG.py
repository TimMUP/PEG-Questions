while True:
    ans = input()
    if ans == "CU":
        print("see you")
    elif ans == ":-)":
        print("I'm happy")
    elif ans == ":-(":
        print("I'm unhappy")
    elif ans == ";-)":
        print("wink")
    elif ans == ":-P":
        print("stick out my tongue")
    elif ans == "(~.~)":
        print("sleepy")
    elif ans == "TA":
        print("totally awesome")
    elif ans == "CCC":
        print("Canadian Computing Competition")
    elif ans == "CUZ":
        print("because")
    elif ans == "TY":
        print("thank-you")
    elif ans == "YW":
        print("you're welcome")
    elif ans == "TTYL":
        print("talk to you later")
        break
    else:
        print(ans)